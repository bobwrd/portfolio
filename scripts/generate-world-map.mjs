// One-off generator: converts Natural Earth 110m land topology (world-atlas)
// into a single SVG path string under an equirectangular projection, baked
// into src/pages/verdict/world-map-path.ts.
//
// Why baked: the old 3D globe pulled ~1.8 MB of three.js and fetched textures
// from unpkg at runtime. The 2D map needs only this one path string (~30 KB),
// no runtime deps, no network.
//
// Run manually if the map ever needs regenerating:
//   node scripts/generate-world-map.mjs

import { readFile, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";
import { feature } from "topojson-client";

const require = createRequire(import.meta.url);
const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

// Projection constants — must match VerdictMap.tsx.
export const MAP_W = 1000;
const LAT_TOP = 84; // crop polar emptiness (north)
const LAT_BOTTOM = -56; // crop Antarctica (south)
export const MAP_H = ((LAT_TOP - LAT_BOTTOM) / 360) * MAP_W; // equirectangular

function project([lng, lat]) {
  const x = ((lng + 180) / 360) * MAP_W;
  const y = ((LAT_TOP - lat) / (LAT_TOP - LAT_BOTTOM)) * MAP_H;
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
}

async function main() {
  const topoPath = require.resolve("world-atlas/land-110m.json");
  const topo = JSON.parse(await readFile(topoPath, "utf8"));
  const land = feature(topo, topo.objects.land);

  let d = "";
  for (const geom of land.features ?? [land]) {
    const polys =
      geom.geometry.type === "Polygon"
        ? [geom.geometry.coordinates]
        : geom.geometry.coordinates;
    for (const poly of polys) {
      for (const ring of poly) {
        // Skip rings entirely inside the cropped polar bands (Antarctica).
        if (ring.every(([, lat]) => lat < LAT_BOTTOM || lat > LAT_TOP)) continue;
        let started = false;
        let prev = null;
        for (const coord of ring) {
          const [x, y] = project(coord);
          // Light decimation: drop points <0.7px from the previous one.
          if (prev && Math.abs(x - prev[0]) < 0.7 && Math.abs(y - prev[1]) < 0.7) continue;
          d += started ? `L${x} ${y}` : `M${x} ${y}`;
          started = true;
          prev = [x, y];
        }
        if (started) d += "Z";
      }
    }
  }

  const out = `// AUTO-GENERATED by scripts/generate-world-map.mjs — do not edit by hand.
// Natural Earth 110m land outline, equirectangular projection,
// cropped to lat [${LAT_BOTTOM}, ${LAT_TOP}], viewBox ${MAP_W} x ${MAP_H.toFixed(2)}.

export const MAP_W = ${MAP_W};
export const MAP_H = ${MAP_H.toFixed(4)};
export const LAT_TOP = ${LAT_TOP};
export const LAT_BOTTOM = ${LAT_BOTTOM};

export const WORLD_PATH = ${JSON.stringify(d)};
`;

  const outPath = join(ROOT, "src", "pages", "verdict", "world-map-path.ts");
  await writeFile(outPath, out);
  console.log(`[map] wrote ${outPath} — path length ${d.length} chars`);
}

main();
